public class Calculadora {
    public static void soma(int n1, int n2) {
        System.out.println("A soma dos parametros: " + (n1 + n2));
    }
    public static void subtrair(int n1, int n2) {
        System.out.println("A subtracao dos parametros " + (n1 - n2));
    }
    public static void multiplicar(int n1, int n2) {
        System.out.println("a multiplicacao dos parametros e " + (n1*n2));
    }
    public static void dividir(int n1, int n2) {
        System.out.println("divisao " + (n1/n2));
    }
}
